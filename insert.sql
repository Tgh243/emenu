DELIMITER $$
CREATE FUNCTION get_id(
	name VARCHAR(255),
    table_name VARCHAR(255)
)
RETURNS INT
DETERMINISTIC
BEGIN
    DECLARE returned_id INT;
	SET returned_id =
        CASE table_name
            WHEN 'Category' THEN (SELECT id FROM Category WHERE Category.name = name)
            WHEN 'Origin' THEN (SELECT id FROM Origin WHERE Origin.name = name)
            WHEN 'Unit' THEN (SELECT id FROM Unit WHERE Unit.name = name)
            WHEN 'Dish' THEN (SELECT id FROM Dish WHERE Dish.name = name)
            WHEN 'Ingredient' THEN (SELECT id FROM Ingredient WHERE Ingredient.name = name)
            ELSE NULL
        END;

    IF returned_id IS NULL THEN
        SIGNAL SQLSTATE '45000' SET MESSAGE_TEXT = 'Invalid table name or name not found';
    END IF;

	RETURN (returned_id);
END$$
